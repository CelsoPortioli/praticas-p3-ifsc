package ifsc;

public class Animal {

	private String Nome;
	private String Raca;
	public String Caminha;
	private Float Comprimento;
	private Integer NumeroDePatas;
	private String Cor;
	private String Ecossistema;
	public String getNome() {
		return Nome;
	}
	public void setNome(String nome) {
		Nome = nome;
	}
	public String getRaca() {
		return Raca;
	}
	public void setRaca(String raca) {
		Raca = raca;
	}
	public Float getComprimento() {
		return Comprimento;
	}
	public void setComprimento(Float comprimento) {
		Comprimento = comprimento;
	}
	public String getCaminha() {
		return Caminha;
	}
	public void setCaminha(String caminha) {
		Caminha = caminha;
	}
	public Integer getNumeroDePatas() {
		return NumeroDePatas;
	}
	public void setNumeroDePatas(Integer numeroDePatas) {
		NumeroDePatas = numeroDePatas;
	}
	public String getCor() {
		return Cor;
	}
	public void setCor(String cor) {
		Cor = cor;
	}
	public String getEcossistema() {
		return Ecossistema;
	}
	public void setEcossistema(String ecossistema) {
		Ecossistema = ecossistema;
	}
	

}
